public class App {
    public static void main(String[] args) throws Exception {
        
        // for loop = executes a block of code a limited amount of times

        for (int index = 0; index < 10; index++) {
            System.out.println(index);
        }

        for (int j = 9; j >=0; j--) {
            System.out.println(j);
        }
    }
}

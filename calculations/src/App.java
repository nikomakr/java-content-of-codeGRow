public class App {
    public static void main(String[] args) throws Exception {
        // expression = operands & operators
        // operands = values, variables, numbers, quantity
        // operators = + - * / %

        int friends = 10;

        friends = friends / 2;
        System.out.println(friends);

        friends++;
        System.out.println(friends);

        friends--;
        System.out.println(friends);

        friends += 5;
        System.out.println(friends);

        friends = friends * 2;
        System.out.println(friends);
    }
}
